  // TC-AC-15-006 / TC-FR-15-006 / TC-NFR-08-004
  test("StatCards refresh within 5 seconds after student API check-in", async ({ page }) => {
    await page.setViewportSize({ width: 1280, height: 720 });
    await loginPreviewInstructorViaForm(page);
    await page.goto(`/sessions/sess-1?tab=monitor`);
    await expect(page.getByTestId("session-monitor-dashboard")).toBeVisible({ timeout: 15_000 });

    const rosterRes = await page.request.get(
      `${API_BASE_URL}/sessions/${SESSION_ACTIVE}/attendance`,
    );
    expect(rosterRes.ok()).toBeTruthy();
    const roster = (await rosterRes.json()) as {
      records: Array<{ id: string; institutionalId: string; status: string }>;
    };
    const target =
      roster.records.find((record) => record.institutionalId === "SV2026001") ??
      roster.records[0];
    expect(target).toBeTruthy();
    const presentCard = page.getByTestId("stat-card-present").locator("p.font-display");
    const initialPresent = Number(
      (await presentCard.innerText()).split("/")[0]?.trim() ?? "0",
    );

    if (target!.status === "Present") {
      const prep = await page.request.patch(`${API_BASE_URL}/attendance/${target!.id}`, {
        data: { status: "Absent", note: "Chuẩn bị kiểm thử monitor poll" },
      });
      expect(prep.ok()).toBeTruthy();
      await expect
        .poll(
          async () => Number((await presentCard.innerText()).split("/")[0]?.trim() ?? "0"),
          { timeout: 6_500, intervals: [500] },
        )
        .toBe(initialPresent - 1);
    }

    const baselinePresent = Number(
      (await presentCard.innerText()).split("/")[0]?.trim() ?? "0",
    );

    const studentApi = await playwrightRequest.newContext();
    const studentLogin = await studentApi.post(`${API_BASE_URL}/auth/login`, {
      data: PREVIEW_CREDENTIALS.student,
    });
    expect(studentLogin.ok()).toBeTruthy();

    const t0 = Date.now();
    const checkIn = await studentApi.post(`${API_BASE_URL}/check-in`, {
      data: { tokenId: VALID_QR_TOKEN_ID, ...CHECK_IN_COORDS },
    });
    const checkInBody = (await checkIn.json()) as { outcome: string };
    expect(checkIn.ok()).toBeTruthy();
    expect(checkInBody.outcome).toBe("Success");
    await studentApi.dispose();

    await expect
      .poll(
        async () => Number((await presentCard.innerText()).split("/")[0]?.trim() ?? "0"),
        { timeout: 6_500, intervals: [250] },
      )
      .toBe(baselinePresent + 1);

    expect(Date.now() - t0).toBeLessThanOrEqual(6_500);
    await expect(
      page.getByTestId(`monitor-row-${target!.institutionalId.toLowerCase()}`),
    ).toContainText("Có mặt");
    await expect(page.getByTestId("monitor-poll-status")).toHaveText(/Cập nhật lúc/);
  });

  // TC-AC-15-007 / TC-FR-15-007
  test("monitor roster sorts by Trạng thái ascending", async ({ page }) => {
    await page.setViewportSize({ width: 1280, height: 720 });
    await loginPreviewInstructorViaForm(page);
    await page.goto(`/sessions/sess-1?tab=monitor`);
    await expect(page.getByTestId("session-monitor-dashboard")).toBeVisible({ timeout: 15_000 });
    await page.getByTestId("monitor-sort-status").click();
    await expect(page.getByTestId("monitor-sort-status")).toHaveAttribute("aria-sort", "ascending");

    const statuses = await page.locator('[data-testid^="monitor-row-"] td:nth-child(3)').allInnerTexts();
    const orderIndex = (label: string) => {
      if (label.includes("Chưa điểm danh") || label.includes("Chờ")) return 0;
      if (label.includes("Vắng")) return 1;
      if (label.includes("Có mặt")) return 2;
      if (label.includes("Có phép")) return 3;
      return 99;
    };
    const indices = statuses.map((s) => orderIndex(s.trim()));
    expect(indices).toEqual([...indices].sort((a, b) => a - b));
  });

