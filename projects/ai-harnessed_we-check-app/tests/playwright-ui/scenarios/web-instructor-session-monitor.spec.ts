  // TC-NFR-08-009
  test("monitor auto-polls on active session and stops on closed session", async ({ page }) => {
    await page.setViewportSize({ width: 1280, height: 720 });
    await loginPreviewInstructorViaForm(page);
    await page.goto(`/sessions/sess-1?tab=monitor`);
    await expect(page.getByTestId("monitor-poll-status")).toHaveText(/Cập nhật lúc/, {
      timeout: 15_000,
    });

    const firstPoll = await page.getByTestId("monitor-poll-status").innerText();
    await page.waitForTimeout(5_500);
    const secondPoll = await page.getByTestId("monitor-poll-status").innerText();
    expect(secondPoll).not.toBe(firstPoll);

    const closedRequests: string[] = [];
    page.on("request", (req) => {
      if (req.url().includes("/attendance")) closedRequests.push(req.url());
    });
    await page.goto(`/sessions/${SESSION_CLOSED}/monitor`);
    await expect(page.getByRole("alert").filter({ hasText: "Buổi học đã kết thúc" })).toBeVisible();
    const countAfterLoad = closedRequests.length;
    await page.waitForTimeout(10_000);
    expect(closedRequests.length).toBeLessThanOrEqual(countAfterLoad + 1);
  });

